package demo;

import java.util.ArrayList;

public class ReverseExample {

	public static void main(String[] args) {
		ArrayList<String> name = new ArrayList<String>();
		name.add("Manaswin");
		name.add("Akshai");
		name.add("Manoj");
		name.add("Ganesh");
		name.add("Hussain");
		System.out.println(name);
		System.out.println(name.reversed());
	}

}
